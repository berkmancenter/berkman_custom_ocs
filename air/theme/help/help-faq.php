<h3>Q: How do I change the language of my theme?</h3>

<p>A: The <code>languages</code> folder contains a .pot file with translatable strings. Open and edit this file in a program such as <a href="http://www.poedit.net/" target="_blank">PoEdit</a>. Be sure to save it for your locale. As an example, a Swedish user would save their translations as a <em>sv_SE.po</em> file in the languages folder.</p>



