<h3>Q: Errors are appearing in my theme. What do I do?</h3>

<p>A: In most cases, this is a result of a plugin conflict. Disable all of your plugins and see if the error goes away. If it does, activate your plugins one by one to see which one is causing the issue. If the error remains with all plugins deactivated, use our support forums to report the issue.</p>



